# ARETMATIC OPPRATIONS
num1 = 10
num2 = 20
print(num1 + num2)
print(num1 - num2)
print(num1 * num2)
print(num1 / num2)
# RELATIONAL OPERATIONS
print(num1 < num2)
print(num1 > num2)
print(num1 == num2)
print(num1 != num2)
# LOGIACL OPERATIONS
log1 = True
log2 = False
# &
print(log1 & log2)
print(log2 & log1)
print(log2 & log2)
print(log1 & log1)
# |

print(log1 | log2)
print(log2 | log1)
print(log2 | log2)
print(log1 | log1)